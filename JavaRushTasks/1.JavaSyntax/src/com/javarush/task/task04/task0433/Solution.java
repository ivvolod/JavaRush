package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        int i = 10;

        while (i >0){
            i--;

            int j = 10;
            while (j >0){
                j--;
                System.out.print("S");
            }System.out.println();
        }

    }
}
